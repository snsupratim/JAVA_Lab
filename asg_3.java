public class asg_3 {
    public static void main(String[] args) {
        // Test data
        double radius = 7.5;

        // Calculate perimeter
        double perimeter = 2 * Math.PI * radius;

        // Calculate area
        double area = Math.PI * radius * radius;

        // Print results
        System.out.println("Perimeter is = " + perimeter);
        System.out.println("Area is = " + area);
    }
}
